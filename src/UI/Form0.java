    private void btnrun2(){
        if(running)return;
        Cript2010x8b.md5.setPass(jPasswordField1.getText());
        running=true;
        x=new Thread() {            
            @Override
            public void run() {
                try{
                    singleton.jButton1.setVisible(true);
                    if(singleton.cb_genRandHash.isSelected()){
                        hash=Hash.genRandom();
                    }
                    if("".equals(data)){
                        singleton.txt_log.setText(singleton.txt_log.getText()+"missing data"+"\n");
                    }else
                    if(hash!=null){
                        program=null;
                        try{
                            boolean crypt=singleton.rb_cript.isSelected();
                            if(crypt){
                                int nCript=Integer.valueOf(singleton.jTextField3.getText());
                                int divisor=3;
                                int div=nCript/divisor;
                                int missing=nCript%divisor;
                                int real;
                                String dataBKP=data;
                                for(int i=0;i<=div;i++){
                                    if(i==div)
                                        real=missing;
                                    else
                                        real=divisor;
                                    int m=0;
                                    do{
                                        m++;
                                        program=new Cript2010x8b(real,crypt,hash,dataBKP,singleton.cb_hashInFile.isSelected()); 
                                        if(m>13&&!program.compareResult)throw new HashException("Sorry, cant handle this data.");
                                    }while(!program.compareResult);
                                    dataBKP=program.getData();
                                    Cript2010x8b.totalSteeps=Cript2010x8b.totalSteeps/divisor*nCript;
                                }
                            }else
                                program=new Cript2010x8b(Integer.valueOf(singleton.jTextField3.getText()),singleton.rb_cript.isSelected(),hash,data,singleton.cb_hashInFile.isSelected()); 
                            if(singleton.rb_cript.isSelected()){
                                if(singleton.combo_language.getSelectedIndex()==0){
                                    if(Cript2010x8b.CHECKDATALOSS)
                                        singleton.txt_log.setText(singleton.txt_log.getText()+"Criptografado: "+program.percetage+"%\n");
                                    else
                                        singleton.txt_log.setText(singleton.txt_log.getText()+"Criptografado!\n");
                                }else{
                                    if(Cript2010x8b.CHECKDATALOSS)
                                        singleton.txt_log.setText(singleton.txt_log.getText()+"Cripted: "+program.percetage+"%\n");
                                    else
                                        singleton.txt_log.setText(singleton.txt_log.getText()+"Cripted!\n");
                                }
                            }else{
                                 if(singleton.combo_language.getSelectedIndex()==0){
                                    singleton.txt_log.setText(singleton.txt_log.getText()+"Descriptografado!"+"\n");
                                }else{
                                    singleton.txt_log.setText(singleton.txt_log.getText()+"Decripted!"+"\n");
                                }
                            }
                            JFileChooser saveFile=null;
                            try{
                                saveFile = new JFileChooser();
                                try{saveFile.setSelectedFile(new File(singleton.jTextField4.getText()));}catch(Exception e){}
                                if (saveFile.showSaveDialog(jPanel1) == JFileChooser.APPROVE_OPTION) {
                                    IO.saveFile(program.getData(),saveFile.getSelectedFile().getAbsolutePath());
                            }
                            }catch(Exception e){
                                singleton.txt_log.setText(singleton.txt_log.getText()+e.getMessage()+"\n");
                            }
                            if(!singleton.cb_hashInFile.isSelected()&&singleton.rb_cript.isSelected()){
                                try{
                                    String ext="";
                                    String filename=saveFile.getSelectedFile().getName();
                                    for(int i=0;i<filename.length();i++){
                                        if(filename.charAt(i)=='.'){
                                            filename=filename.substring(0, i);
                                            ext=filename.substring(i);
                                            break;
                                        }
                                    }
                                    IO.saveFile(hash.compressHash(), saveFile.getSelectedFile().getAbsolutePath().replace(filename, filename+"-hash("+singleton.jTextField3.getText()+")"));//+ext));
                                }catch(Exception e){
                                    singleton.txt_log.setText(singleton.txt_log.getText()+e+"\n");
                                }
                            }
                        }catch(Exception e){
                            singleton.txt_log.setText(singleton.txt_log.getText()+e+"\n");
                        }
                    }else{
                        singleton.txt_log.setText(singleton.txt_log.getText()+"missing hash"+"\n");
                    }
                }catch(Exception e){
                    x.interrupt();x.stop();
                }finally{
                    //data="";
                    Cript2010x8b.totalSteeps=1;
                    Cript2010x8b.currentSteeps=0;
                    Cript2010x8b.computingSteeps=1;
                    running=false;
                    singleton.jButton1.setVisible(false);
                }
            }
        };
        x.setPriority(Thread.MAX_PRIORITY);
        x.start();
    }
    
