    private void btn_openfileMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_openfileMouseReleased
        this.data="";
        String path="";
        try{
            JFileChooser openFile = new JFileChooser();
            if (openFile.showOpenDialog(jPanel1) == JFileChooser.APPROVE_OPTION) {
                this.jTextField4.setText(openFile.getSelectedFile().getPath());
                path=openFile.getSelectedFile().getPath();
                this.data=IO.readFileAsString(openFile.getSelectedFile().getPath());
            }
        }catch(Exception e){
            this.txt_log.setText(this.txt_log.getText()+e+"\n");
        }
        if(SAVEBKP&&!"".equals(path)){
            try{
                String ext="";
                String file="";
                for(int i=0;i<path.length();i++){
                    if(path.charAt(i)=='.'){
                        file=path.substring(0, i);
                        ext=path.substring(i);
                        break;
                    }
                }
                IO.saveFile(data, file+"BKP"+ext);
            }catch(Exception e){}
        }
    }//GEN-LAST:event_btn_openfileMouseReleased

